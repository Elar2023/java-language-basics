package lesson2.homework.EX02A;

public class EX02A {

    public static String compare(double num) {
        if (num > 5) {
            return "The input number is bigger than 5!";
        } else if (num < 5) {
            return "The input number is smaller than 5!";
        }
        return "The input number is 5!";
    }
}
